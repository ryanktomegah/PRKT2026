@dataclass
class KafkaConfig:
    """Producer/consumer Kafka connection configuration for LIP.

    All fields default to production-safe values that enforce
    exactly-once semantics (EOS) via idempotent producers and
    ``isolation.level=read_committed`` consumers.

    Attributes:
        bootstrap_servers: List of ``host:port`` broker addresses.
        security_protocol: Kafka security protocol (default ``'SSL'``).
        ssl_ca_location: Path to the CA certificate for TLS verification.
        ssl_cert_location: Path to the client certificate for mTLS.
        ssl_key_location: Path to the client private key for mTLS.
        enable_idempotence: Enables producer idempotence (required for EOS).
        acks: Producer acknowledgement mode; ``'all'`` ensures ISR durability.
        max_in_flight_requests_per_connection: Must be ``1`` for exactly-once
            ordering guarantees.
        transactional_id: Optional transactional producer ID; set per-instance
            when using Kafka transactions (e.g., C7 decision logging).
    """

    bootstrap_servers: List[str] = field(default_factory=lambda: ["kafka:9092"])
    security_protocol: str = "SSL"
    ssl_ca_location: str = "/etc/ssl/kafka/ca.crt"
    ssl_cert_location: str = "/etc/ssl/kafka/client.crt"
    ssl_key_location: str = "/etc/ssl/kafka/client.key"
    enable_idempotence: bool = True  # exactly-once
    acks: str = "all"
    max_in_flight_requests_per_connection: int = 1  # exactly-once requires this
    transactional_id: Optional[str] = None

    def to_producer_config(self) -> dict:
        """Build a confluent-kafka / librdkafka producer configuration dict.

        Includes all TLS, idempotence, and ack settings.  When
        :attr:`transactional_id` is set, adds ``transactional.id`` for
        transactional producer use.

        Returns:
            Dict of librdkafka configuration key-value pairs suitable for
            passing directly to ``confluent_kafka.Producer(config)``.
        """
        cfg = {
            "bootstrap.servers": ",".join(self.bootstrap_servers),
            "security.protocol": self.security_protocol,
            "ssl.ca.location": self.ssl_ca_location,
            "ssl.certificate.location": self.ssl_cert_location,
            "ssl.key.location": self.ssl_key_location,
            "enable.idempotence": self.enable_idempotence,
            "acks": self.acks,
            "max.in.flight.requests.per.connection": self.max_in_flight_requests_per_connection,
        }
        if self.transactional_id:
            cfg["transactional.id"] = self.transactional_id
        return cfg

    def to_consumer_config(self, group_id: str) -> dict:
        """Build a confluent-kafka / librdkafka consumer configuration dict.

        Sets ``enable.auto.commit=False`` and
        ``isolation.level=read_committed`` to enforce exactly-once
        consumption semantics in conjunction with transactional producers.

        Args:
            group_id: Kafka consumer group identifier for this consumer
                instance.

        Returns:
            Dict of librdkafka configuration key-value pairs suitable for
            passing directly to ``confluent_kafka.Consumer(config)``.
        """
        return {
            "bootstrap.servers": ",".join(self.bootstrap_servers),
            "security.protocol": self.security_protocol,
            "ssl.ca.location": self.ssl_ca_location,
            "ssl.certificate.location": self.ssl_cert_location,
            "ssl.key.location": self.ssl_key_location,
            "group.id": group_id,
            "auto.offset.reset": "earliest",
            "enable.auto.commit": False,  # manual commit for exactly-once
            "isolation.level": "read_committed",
        }
