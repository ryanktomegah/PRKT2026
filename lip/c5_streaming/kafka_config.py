"""
kafka_config.py — Kafka topic definitions and partition strategies
C5 Spec: Exactly-once semantics, payment event processing
"""
from dataclasses import dataclass, field
from enum import Enum
from typing import Dict, List, Optional


class KafkaTopic(str, Enum):
    """Canonical Kafka topic names for the LIP platform.

    All topic names are prefixed ``lip.`` to namespace them within a shared
    cluster.  Partition keys default to ``uetr`` for event ordering per
    payment.

    Attributes:
        PAYMENT_EVENTS: Inbound ISO 20022 pacs.002 / FedNow / RTP / SEPA
            events from bank connectors.  24 partitions for throughput.
        FAILURE_PREDICTIONS: C1 classifier output with ``failure_probability``
            and SHAP features.  12 partitions.
        SETTLEMENT_SIGNALS: SWIFT / camt.054 settlement confirmations
            consumed by C3 Repayment Engine.  24 partitions.
        DISPUTE_RESULTS: C4 Dispute Classifier output.  6 partitions.
        VELOCITY_ALERTS: C6 AML velocity limit breaches.  6 partitions.
        LOAN_OFFERS: Outbound bridge-loan offers generated by C7.  6 partitions.
        REPAYMENT_EVENTS: C3 repayment confirmations.  6 partitions.
        DECISION_LOG: Immutable audit records for every LIP decision.
            **7-year retention** (``retention_ms = 7 * 365 * 24 * 3600 * 1000``)
            mandated by SR 11-7 model governance and EU AI Act Art.17.
            12 partitions.
        DEAD_LETTER: Unprocessable events from any topic.  6 partitions.
    """

    PAYMENT_EVENTS = "lip.payment.events"
    FAILURE_PREDICTIONS = "lip.failure.predictions"
    SETTLEMENT_SIGNALS = "lip.settlement.signals"
    DISPUTE_RESULTS = "lip.dispute.results"
    VELOCITY_ALERTS = "lip.velocity.alerts"
    LOAN_OFFERS = "lip.loan.offers"
    REPAYMENT_EVENTS = "lip.repayment.events"
    DECISION_LOG = "lip.decision.log"
    DEAD_LETTER = "lip.dead.letter"
    STRESS_REGIME = "lip.stress.regime"


@dataclass
class TopicConfig:
    """Configuration for a single Kafka topic.

    Attributes:
        topic: :class:`KafkaTopic` enum member this config applies to.
        partitions: Number of partitions.  Higher values increase throughput
            at the cost of consumer rebalance latency.
        replication_factor: Number of broker replicas (default 3 for
            production-grade durability).
        retention_ms: Log retention in milliseconds.  Default is 7 days
            (``7 * 24 * 3600 * 1000``).  **Exception**: ``DECISION_LOG``
            uses 7 years (``7 * 365 * 24 * 3600 * 1000``) for compliance.
        exactly_once: When ``True``, producers must use idempotent writes
            and transactional commits (``enable.idempotence=True``).
        partition_key: Message field used as the partition key.  Defaults
            to ``'uetr'`` to guarantee ordering of all events for a single
            payment on the same partition.
    """

    topic: KafkaTopic
    partitions: int
    replication_factor: int = 3
    retention_ms: int = 7 * 24 * 3600 * 1000  # 7 days default
    exactly_once: bool = True
    partition_key: str = "uetr"  # partition by UETR for ordering


